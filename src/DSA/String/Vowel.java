package DSA.String;

public class Vowel {
    public static void main(String[] args) {
        char ch = 'b';
        if(ch=='a'||ch=='e'|| ch=='i'|| ch=='o' || ch=='u'){
            System.out.println("character is vowel");
        }
        else {
            System.out.println("character is not a vowel");
        }
    }

}
